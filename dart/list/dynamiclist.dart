void main() {
  // List of strings containing names
  List<String> names = ["ram", "shyam", "hari", "krishna"];
  print(names); // Output: [ram, shyam, hari, krishna]

  // List of integers
  List<int> numbers = [1, 2, 3, 4, 5];
  print(numbers); // Output: [1, 2, 3, 4, 5]

  // List with multiple data types (dynamic list)
  List nultivalue = [20, 40, 50, "ram", "shyam", "babu"];
  print(nultivalue); // Output: [20, 40, 50, ram, shyam, babu]

  // Another list of integers using 'var'
  var a = [10, 30, 50, 60, 66, 33, 90, 80];
  print(a); // Output: [10, 30, 50, 60, 66, 33, 90, 80]

  // List of fruits
  List fruits = ['Apple', 'Orange', 'Banana', 'Grapes', 'Mango', 'Cherry'];
  print(fruits); // Output: [Apple, Orange, Banana, Grapes, Mango, Cherry]
}
